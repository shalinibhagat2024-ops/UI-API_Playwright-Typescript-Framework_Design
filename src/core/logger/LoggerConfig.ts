/**
 * Logger configuration.
 * Future versions can load this from JSON.
 */
export class LoggerConfig {
  /**
   * Enable or disable debug logs.
   */
  public static readonly enableDebug = true;

  /**
   * Print timestamp.
   */
  public static readonly showTimestamp = true;

  /**
   * Print current environment.
   */
  public static readonly showEnvironment = true;
}
